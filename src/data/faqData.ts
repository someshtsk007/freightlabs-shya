export const getFAQsByCategory = (category: string) => {
  if (category === 'all') return faqData;
  return faqData.filter((faq) => faq.category === category);
};

export const getFeaturedFAQs = (limit?: number) => {
  const featured = faqData.filter((faq) => faq.isFeatured);
  return limit ? featured.slice(0, limit) : featured;
};

export const searchFAQs = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return faqData.filter(
    (faq) =>
      faq.question.toLowerCase().includes(lowercaseQuery) ||
      faq.answer.toLowerCase().includes(lowercaseQuery) ||
      faq.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery))
  );
};
